public class Logicals {
    public static void main(String[]args)
    {
        boolean x=true, y=false;
        //Logical and
        System.out.println(x && y);
        // Logical Or
        System.out.println(x || y);
        /*Logical Nor
        System.out.println(x ! y);
*/
// XOR
System.out.println(x ^ y);


    }
}
